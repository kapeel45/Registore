package com.registore.apartment.register;

public class ApartmentRegisterDto {

	private String apartmentName;
	
	private String noOfWings;
	
	private String wingsName;
	
	private String noOfFloors;
	
	private String noOfFlats;
	
	private String noOfFlatsOnEachFloor;
	
	private String location;
	
	private String longitude;
	
	private String latitude;

	public String getApartmentName() {
		return apartmentName;
	}

	public void setApartmentName(String apartmentName) {
		this.apartmentName = apartmentName;
	}

	public String getNoOfWings() {
		return noOfWings;
	}

	public void setNoOfWings(String noOfWings) {
		this.noOfWings = noOfWings;
	}

	public String getWingsName() {
		return wingsName;
	}

	public void setWingsName(String wingsName) {
		this.wingsName = wingsName;
	}

	public String getNoOfFloors() {
		return noOfFloors;
	}

	public void setNoOfFloors(String noOfFloors) {
		this.noOfFloors = noOfFloors;
	}

	public String getNoOfFlats() {
		return noOfFlats;
	}

	public void setNoOfFlats(String noOfFlats) {
		this.noOfFlats = noOfFlats;
	}

	public String getNoOfFlatsOnEachFloor() {
		return noOfFlatsOnEachFloor;
	}

	public void setNoOfFlatsOnEachFloor(String noOfFlatsOnEachFloor) {
		this.noOfFlatsOnEachFloor = noOfFlatsOnEachFloor;
	}

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	public String getLatitude() {
		return latitude;
	}

	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

}